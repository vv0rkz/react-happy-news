export const isPositiveNews = (title: string, description: string = ''): boolean => {
  const text = `${title} ${description}`.toLowerCase()

  // Сначала отсекаем негативные
  const hasNegative = NEGATIVE_KEYWORDS.some((keyword) => text.includes(keyword))

  if (hasNegative) return false

  // Требуем наличие позитивных слов
  const hasPositive = POSITIVE_KEYWORDS.some((keyword) => text.includes(keyword))

  return hasPositive
}
